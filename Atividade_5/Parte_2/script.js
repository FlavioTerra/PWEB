let num1 = parseFloat(prompt('Digite o primeiro numero: '));
let num2 = parseFloat(prompt('Digite o segundo numero: '));

alert('A soma dos numeros e igual a: ' + (num1 + num2));
alert('A subtração do primeiro pelo segundo e igual a: ' + (num1 - num2));
alert('A divisao do primeiro numero pelo segundo e igual a: ' + (num1 / num2));
alert('O resto da divisao do primeiro numero pelo segundo: ' + (num1 % num2));